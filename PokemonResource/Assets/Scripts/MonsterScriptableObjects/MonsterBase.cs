using System.Collections;
using System.Collections.Generic;
using UnityEngine;





[CreateAssetMenu(fileName = "Monster", menuName = "Monster/Create new Monster")]
public class MonsterBase : ScriptableObject
{
    [Tooltip("The name of the monster")]
    [SerializeField] string monsterName;


    [Tooltip("A description of the monster")]
    [TextArea]
    [SerializeField] string description;


    [Space(2)]
    [SerializeField] Sprite frontSprite;
    [SerializeField] Sprite backSprite;

    [Space(2)]
    [Tooltip("The type of the monster")]
    [SerializeField] MonsterType type1;

    [Tooltip("The secondary type of the monster")]
    [SerializeField] MonsterType type2;

    [Space(2)]
    [Tooltip("A list of move the monster learns naturally")]
    [SerializeField] List<LearnableMove> learnableMoves;

    public static int MaxNumOfMoves {get; set;} = 4;



    public int GetExpForLevel(int level)
    {
        if(growthRate == GrowthRate.Fast)
        {
            return 4 * (level* level * level) / 5;
        }
        else if(growthRate == GrowthRate.MediumFast)
        {
            return level * level * level;
        }

        return -1; 
    }

    [Space(2)]
    [Header("Monster Battle Stats")]
   

    // Base Stats
    [SerializeField] int maxHp;
    [SerializeField] int attack;
    [SerializeField] int defense;
    [SerializeField] int spAttack;
    [SerializeField] int spDefense;
    [SerializeField] int speed;

    [Space(2)]
    [Header("Catch Rate & Exp Yield ")]
    [SerializeField]
    private int catchRate;

    [SerializeField]

    private int expYield;

    [SerializeField]
    GrowthRate growthRate;


    public int CatchRate => catchRate;

    public int ExpYield => expYield;

    public GrowthRate  GrowthRate=> growthRate;

    #region Monster Information
    public string Name
    {
        get { return monsterName; }
    }

    public string Description
    {
        get { return description; }
    }

    public Sprite FrontSprite
    {
        get { return frontSprite; }
    }

    public Sprite BackSprite
    {
        get { return backSprite; }
    }

    public MonsterType Type1
    {
        get { return type1; }
    }

    public MonsterType Type2
    {
        get { return type2; }
    }

    public int MaxHp
    {
        get { return maxHp; }
    }

    public int Attack
    {
        get { return attack; }
    }

    public int SpAttack
    {
        get { return spAttack; }
    }

    public int Defense
    {
        get { return defense; }
    }

    public int SpDefense
    {
        get { return spDefense; }
    }

    public int Speed
    {
        get { return speed; }
    }

    public List<LearnableMove> LearnableMoves
    {
        get { return learnableMoves; }
    }


    #endregion
}



[System.Serializable]
public class LearnableMove
{
    [SerializeField] MoveBase moveBase;
    [SerializeField] int level;

    public MoveBase Base
    {
        get { return moveBase; }
    }

    public int Level
    {
        get { return level; }
    }
}

public enum MonsterType
{
    None,
    Normal,
    Fire,
    Water,
    Electric,
    Grass,
    Ice,
    Fighting,
    Poison,
    Ground,
    Flying,
    Psychic,
    Bug,
    Rock,
    Ghost,
    Dragon
}

public enum Stat
{
    Attack,
    Defense,
    SpAttack,
    SpDefense,
    Speed,

    // These 2 are not actual stats, they're used to boost the moveAccuracy
    Accuracy,
    Evasion
}


public enum GrowthRate
{
    Fast,
    MediumFast
}

